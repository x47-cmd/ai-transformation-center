  function normalize(value) {
    return String(value ?? "")
      .trim()
      .toLowerCase()
      .replace(/_/g, " ")
      .replace(/-/g, " ");
  }

  function detectProfile(project = {}) {
    const text = normalize([
      project.title,
      project.titleEn,
      project.description,
      project.department,
      project.category,
      project.type
    ].filter(Boolean).join(" "));

    let selected = null;
    let bestScore = 0;

    PROJECT_PROFILES.forEach(profile => {
      const score = profile.keywords.reduce(
        (sum, keyword) =>
          sum + (text.includes(normalize(keyword)) ? 1 : 0),
        0
      );

      if (score > bestScore) {
        bestScore = score;
        selected = profile;
      }
    });

    return selected;
  }

  function list(items, className = "") {
    return `
      <ul class="${className}">
        ${items
          .map(item => `<li>${escapeHtml(item)}</li>`)
          .join("")}
      </ul>
    `;
  }

  function escapeHtml(value) {
    return String(value ?? "")
      .replace(/&/g, "&amp;")
      .replace(/</g, "&lt;")
      .replace(/>/g, "&gt;")
      .replace(/"/g, "&quot;")
      .replace(/'/g, "&#039;");
  }

